  <h3>Types of Treatment</h3><br>

<p class="T-p">

  In general there are three types of treatment for children’s cancer.
  Firstly there is the surgical procedure (operation). Treatment may also
  be by means of radiation of the tumour area (radiotherapy) and the
  third type of treatment is by administering medication (chemo-therapy
  or immunotherapy). Bone marrow or stem cell transplants are regarded
  as separate treatments. Which of this (these) type(s) of treatment is
  (are) necessary for your child, will depend on the type of cancer and the
  extent of it. Not every type of cancer requires an operation. Sometimes
  radiotherapy or chemotherapy, or a combination of these, is sufficient.
  The sequence of the types of treatment may also vary; sometimes a
  treatment with medication will precede an operation, sometimes it will
  follow the operation. In the first case medication is given in order to try
  and reduce the size of the tumour and any secondary tumours, so that
  they can be removed more easily.

</p>

<br>

  <h3>1: Surgery</h3>

<br>

<p class="T-p">

  If an operation is necessary, the surgeon will usually try to remove the
  tumour completely. Sometimes it will suffice to remove a small part
  of the tumour (biopsy) in order to examine what type of cancer it is.
  The surgeon may be a paediatric surgeon, a paediatric neurosurgeon,
  but also an ENT specialist, an eye specialist, an orthopaedist or a
  paediatric urologist. It is obviously very important that the damage to
  the surrounding healthy tissue is limited as much as possible during the
  operation. If it is possible to remove a (malignant) tumour completely,
  a follow-up treatment is sometimes not necessary. However, in the case
  of certain tumours it is known that immediately after the operation a
  follow-up treatment must be started. Also in the case where tumour cells
  did remain, an extra treatment will be necessary to destroy the cancer
  cells that are still present. The follow-up treatment after an operation
  consists of chemotherapy and/or radiotherapy. In general children can
  deal with major operations well as the body has great recuperative
  powers. Sometimes after an operation children need to be cared for in
  the children’s intensive care (IC) unit. This is a unit where very intensive
  medical and nursing care is given. Most consequences of the operation are temporary. However, there may also be operations necessary that
cause permanent damage to healthy tissue. The possibilities of the
operation and any consequences for your child, as far as these can be
assessed, will be discussed with you in advance by the surgeon involved.

</p>

<br>

<h3>2: Radiotherapy</h3>

<br>

<p class="T-p">

  For some types of cancer radiation (radiotherapy) is the most appropriate
  treatment, on its own or as part of the treatment. Usually radiotherapy
  will only be given after an operation or after treatment with medication.
  Initially radiation will be aimed at the area where the tumour is located
  or was removed from. It is however also possible that radiotherapy will
  be used on other areas in the body where tumour tissue may be present.
  Radiotherapy usually consists of short daily treatments on weekdays
  ranging from several to six weeks. In most cases during radiation the
  child will need to lie still in a specific position. The child needs to be well
  prepared for this.
  The educational assistant of the children’s department, who is someone
  your child trusts, will have an informative and mainly supporting role
  in the preparation. Usually videos, games or simulation are used to make
  children familiar with the situation. Technicians and the radiotherapy
  department will teach your child to recognise rituals in the treatment
  process and he/she will become comfortable with the surrounding
  machines. In general there are different kinds of radiation. All types
  of radiation are invisible and cannot be felt. Most children tolerate
  radiotherapy well. Symptoms depend upon the area that is treated. The
  main side effects are (depending upon the treated area) apathy, nausea,
  diarrhoea, irritation of the skin or mucous membrane and headaches.
  These symptoms will pass; if necessary we can try to alleviate them with
  medication. Apart from temporary symptoms there may also be permanent
  side effects from radiotherapy. Whether these side effects will occur,
  depends upon the area that is treated. During the treatment you will meet
  a radiotherapy oncologist, a physician who is specialised in radiation.
  The process surrounding the preparation, the effects to be expected and
  possible symptoms of the treatment will be discussed with you.

</p>

<br>

<h3>3: Chemotherapy</h3>

<br>

<p class="T-p">

  Chemotherapy, as a first treatment or as a follow-up treatment, usually
  consists of a number of courses of cytostatics. This is medication that
  has a cell growth inhibiting or cell killing effect. The cytostatics are
  usually administered by injection or infusion; some have to be taken in
  tablet, liquid or capsule form. Sometimes cytostatics are administered
  into the spinal canal in order that it will penetrate well into the central
  nervous system and the cerebral membranes.
  Once the cytostatics have been administered, they will end up in the
  blood. Via the blood they are then spread all through the body. This is
  how cancer cells all over the body are reached. In most cases different
  kinds of cytostatics are combined in a treatment. Unfortunately the
  cytostatics do not only destroy the abnormal cells. Healthy cells in
  the body are also affected. As a result your child may experience side
  effects from the medication. The severity of the symptoms is different
  for each child and for each type of anti-cancer drug. Most side effects
  of chemotherapy are temporary. Symptoms occurring mostly during
  or after the courses are nausea, vomiting and sometimes diarrhoea.
  Temporary hair loss may also occur. We can usually combat symptoms
  such as nausea and vomiting with appropriate medication. As a result of
  the treatment a shortage of healthy blood cells may occur. This is why
  children are more prone to infections during this period. There is also
  a greater possibility of anaemia and an increased risk of bruising and
  spontaneous haemorrhages. If this occurs it is often necessary to give
  a transfusion of blood cells or blood platelets. There is a special leaflet
  about receiving blood products. During the treatment we will keep a
  close eye on your child in order to control side effects as best we can.
  However, it may sometimes be necessary to temporarily slow down or
  suspend the treatment. If it appears that the planned quantity
  of chemotherapy gives too many side effects, it may be decided that
  other cytostatics should be given or that the dosage of the cytostatics
  needs to be reduced. On the other hand, if the treatment is showing too
  few results, it may be decided to increase the dosage of cytostatics.
  Although most side effects will disappear by themselves after a while,
some effects of the illness and the treatment may be permanent. We try
to prevent permanent damage by checking several organs regularly and
carefully. If there is a threat that damage may occur the medication will
be adjusted. If permanent effects cannot be prevented, the attending
paediatric oncologist will discuss this situation with you. In a small
number of cancer types (especially some forms of leukaemia and
lymphoma) a more intensive treatment may have to be followed up by
a lengthy maintenance treatment with medication. This is in order to
reduce the risk of a recurrence of the illness.

</p>

<br>

<h3>4: Bone marrow or stem cell transplant</h3>

<br>

<p class="T-p">

  Sometimes a very intensive treatment is necessary to suppress the
  illness or reduce the risk of recurrence. The healthy bone marrow of a
  child cannot tolerate this intensive treatment. This can therefore only
  take place if healthy young stem cells (basic cells taken from the blood
  or bone marrow) are given (back) after the intensive treatment. In the
  body stem cells can develop into all kinds of blood cells including
  white blood cells (leucocytes), red blood cells (erythrocytes) and blood
  platelets (thrombocytes). Stem cells can be taken from the blood or
  bone marrow of someone else, a donor. This is called allogeneic stem
  cell transplant (ASCT). These allogeneic stem cell transplants in children
  are not performed in the VU University Medical Centre, but in two other
  Dutch stem cell transplant centres (Leiden and Utrecht). In a so-called
  autologous stem cell transplant the stem cells of the child itself are used.
  For that purpose a generous amount of stem cells will have previously
  been taken from the blood or the bone marrow and stored. After the
  intensive treatment this can then be given back. Autologous stem cell
  transplants are performed within the VUmc Kinderoncologisch Centrum.
  If a stem cell transplant is necessary for your child, the attending paediatric
  oncologist will give you further information about the procedure nearer
  the time.

</p>

